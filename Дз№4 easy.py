# Задание-3:
# Дан список, заполненный произвольными числами.
# Получить список из элементов исходного, удовлетворяющих следующим условиям:
# + Элемент кратен 3
# + Элемент положительный
# + Элемент не кратен 4

import random

lst_or = [random.randint(-50, 50) for _ in range(50)]

lst_new = [i for i in lst_or if i % 3 == 0 and i >= 0 and i % 4 != 0 ]


print('original list - ', lst_or)
print('new list - ', lst_new)

