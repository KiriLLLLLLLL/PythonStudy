# Задание-1:
# Дан список, заполненный произвольными целыми числами. 
# Получить новый список, элементы которого будут
# квадратами элементов исходного списка
# [1, 2, 4, 0] --> [1, 4, 16, 0]

import random

lst_or = [random.randint(0, 30) for _ in range(15)]
print('original list - ', lst_or)

lst_new = [i**2 for i in lst_or]
print('new list - ', lst_new)    



